def generate_page_text(player_name: str) -> str:
    return f"""{{{{פרופיל כדורסל
|שם מלא={player_name}
|שם בלועזית=
|גובה=
|עמדה=
|מספר חולצה=
|תאריך לידה=
|מקום לידה=
|שחקן בית=

|מתאזרח=
|שחקן זר=כן
|פרש ממשחק=
|תמונה=
}}}}
"""


def upload_player(player_name: str) -> None:
    logging.info(f'Uploading game from file: {player_name}')
    page_name = f"{BASKETBALL_PREFIX}:{player_name}"
    player_page = pw.Page(site, page_name)

    if player_page.exists():
        logging.info(f'Player page already exists: {page_name}, skipping upload.')
        return

    player_page.text = generate_page_text(player_name)

    player_page.save(summary="MaccabiBot - Uploading Basketball Players", bot=True)


if __name__ == '__main__':
    for name in PLAYERS_NAMES:
        upload_player(name)
