for cat_title, parent in mappings:
    page = pw.Page(site, cat_title)
    parent_tag = f'[[קטגוריה:{parent}]]'
    if not page.exists():
        page.text = parent_tag
        page.save(summary='הגדרת קטגורית הורה', minor=False)
        print(f'CREATED+PARENTED: {cat_title}')
        continue
    if parent_tag in page.text:
        print(f'SKIP (already has parent): {cat_title}')
        continue
    page.text = page.text.rstrip() + f'\n\n{parent_tag}'
    page.save(summary='הוספת קטגורית הורה: ' + parent, minor=False)
    print(f'UPDATED: {cat_title}')
